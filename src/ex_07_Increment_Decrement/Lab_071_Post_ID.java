package ex_07_Increment_Decrement;

public class Lab_071_Post_ID {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(++a); // 11
        System.out.println(a);  // 11

        // Post Increment == Print first anf then increase

        int a_post =10;
        System.out.println(a_post++);  // 10
        System.out.println(a_post);  // 11


    }
}
