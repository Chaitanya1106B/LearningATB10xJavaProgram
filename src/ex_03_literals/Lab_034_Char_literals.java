package ex_03_literals;

public class Lab_034_Char_literals {
    public static void main(String[] args) {


        // Chars- Store
        char c1= 'A';        // this is called character literals
        //char c2= "A";  // String -- multiple chars

        char c2= '@';
        char c3= '_';
        char c4= '9';
        char c5= ' ';

        char new_line = '\n';
        char tab_line = '\t';
        char back_space = '\b';
        char carriage_return = '\r';

        //System.out.println("ChaitanyaB");

        System.out.println("Chaitanya" + new_line + "B");
        System.out.println("Chaitanya" + tab_line + "B");
        System.out.println("Chaitanya" + back_space + "B"); // removes the last character - a
        System.out.println("Chaitanya" + carriage_return + "B"); // removes the 1st word.







    }
}
